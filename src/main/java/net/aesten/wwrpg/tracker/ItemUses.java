package net.aesten.wwrpg.tracker;

public class ItemUses {
    private int uses = 0;
    private int triggers = 0;

    public int getUses() {
        return uses;
    }

    public void setUses(int uses) {
        this.uses = uses;
    }

    public int getTriggers() {
        return triggers;
    }

    public void setTriggers(int triggers) {
        this.triggers = triggers;
    }
}
